import UIKit

extension Constants {
    /// Spacings should be multiples of `8`.
    enum Spacing {
        static let none: CGFloat = 0
        static let small: CGFloat = 8
        static let medium: CGFloat = 16
        static let large: CGFloat = 24
        static let xLarge: CGFloat = 32
    }
}
